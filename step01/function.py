import numpy as np

#----------------------------------------------------
#ガウス分布
def normal(x, mu=0, sigma=1):
    """
    x: 確率変数
    mu, sigma: param
    mu=0, sigma=1なら標準正規分布
    """
    y = 1 / (np.sqrt(2*np.pi) * sigma) * np.exp(-(x-mu)**2 / (2*sigma**2))
    return y

